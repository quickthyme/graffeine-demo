extension UIColor {

    static var blackAlpha: UIColor {
        return UIColor(named: "BlackAlpha")!
    }

    static var blackAlphaInverting: UIColor {
        return UIColor(named: "BlackAlphaInverting")!
    }

    static var inverseLabel: UIColor {
        return UIColor(named: "InverseLabel")!
    }

    static var lavender: UIColor {
        return UIColor(named: "Lavender")!
    }

    static var ocean: UIColor {
        return UIColor(named: "Ocean")!
    }

    static var unleaded: UIColor {
        return UIColor(named: "Unleaded")!
    }

    static var diagonalLines: UIColor {
        return UIColor(patternImage: UIImage(named: "diagonal_lines")!)
    }
}
